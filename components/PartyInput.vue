<template>
<div>
  <div class="field">
  <span class="is-flex"><label class="label">{{ label }}</label><span @click="isOpen = !isOpen" class="icon has-text-info" style="cursor: pointer;"><i class="fas fa-info-circle"></i></span></span>
    <b-field grouped group-multiline :type="type">
      <p class="control">
        <button @click="emitVal('Democrat')" :class="[baseClass, {'is-success': isDem}]">
          <b-icon v-if="isDem" icon="check"></b-icon>
          <span>{{$t('request.party.democratic')}}</span>
        </button>
      </p>
      <p class="control">
        <button @click="emitVal('Republican')" :class="[baseClass, {'is-success': isRep}]">
          <b-icon v-if="isRep" icon="check"></b-icon>
          <span>{{$t('request.party.republican')}}</span>
        </button>
      </p>
      <p class="control">
        <button @click="emitVal('Independent')" :class="[baseClass, {'is-success': isInd}]">
          <b-icon v-if="isInd" icon="check"></b-icon>
          <span>{{$t('request.party.independent')}}</span>
        </button>
      </p>
      <p class="control">
        <button @click="emitVal('none')" :class="[baseClass, {'is-success': isNone}]">
          <b-icon v-if="isNone" icon="check"></b-icon>
          <span>{{$t('request.party.none')}}</span>
        </button>
      </p>
      <p class="control">
        <button @click="isOther = true" :class="[baseClass, {'is-success': isOther}]">
          <b-icon v-if="isOther" icon="check"></b-icon>
          <span>{{$t('request.party.other')}}</span>
        </button>
      </p>
    </b-field>
  </div>
  <b-field v-if="isOther">
    <b-input
      :placeholder="$t('request.party.placeholder')"
      type="text"
      :value="other"
      ref="party"
      @input="emitVal"></b-input>
  </b-field>
  <b-message :title="toolTipTitle" type="is-info" has-icon :active.sync="isOpen">
    <slot name="tooltip"></slot>
  </b-message>
</div>
</template>

<script>
export default {
  name: 'Party-Input',
  props: [
    'value',
    'type',
    'label',
    'message',
    'toolTipTitle'
  ],
  data () {
    return {
      isOther: false,
      other: '',
      baseClass: {
        'is-medium': true,
        'is-outlined': true,
        button: true
      },
      isOpen: false
    }
  },
  computed: {
    isDem () {
      switch (this.value ? this.value.toLowerCase() : '') {
        case 'democrat':
        case 'dem':
        case 'democrats':
          if (!this.isOther) { return true }
          break
        default:
          return false
      }
    },
    isInd () {
      switch (this.value ? this.value.toLowerCase() : '') {
        case 'independent':
        case 'ind':
        case 'ind.':
          if (!this.isOther) { return true }
          break
        default:
          return false
      }
    },
    isRep () {
      switch (this.value ? this.value.toLowerCase() : '') {
        case 'republican':
        case 'rep':
          if (!this.isOther) { return true }
          break
        default:
          return false
      }
    },
    isNone () {
      switch (this.value ? this.value.toLowerCase() : '') {
        case 'none':
        case 'unaffiliated':
          if (!this.isOther) { return true }
          break
        default:
          return false
      }
    }
  },
  methods: {
    emitVal (val) {
      // console.log(val)
      if (val === 'Republican') {
        this.$store.commit('requests/update', {joinDa: null})
      }
      switch (val) {
        case 'Democrat':
        case 'Republican':
        case 'Independent':
        case 'none':
          this.other = ''
          this.isOther = false
          this.$emit('input', val)
          break
        default:
          this.$emit('input', this.other)
      }
      // console.log('val', val || '', 'input', this.$refs.party.value || '')
      // if (this.isRep || this.isDem || this.isNone || this.isInd) {
      //   this.other = ''
      // }
      // this.$emit('input', val)
    }
  }
}
</script>
